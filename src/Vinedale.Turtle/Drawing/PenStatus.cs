namespace Vinedale.Turtle.Drawing
{
    /// <summary>
    /// An enum indicating if the turtle's pen is up or down.
    /// </summary>
    public enum PenStatus
    {
        /// <summary>
        /// Pen down (ie, drawing).
        /// </summary>
        Down,

        /// <summary>
        /// Pen up (ie, not drawing).
        /// </summary>
        Up,
    }
}
